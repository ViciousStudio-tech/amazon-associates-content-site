# ── Colors ────────────────────────────────────────────────────────────────────
class C:
    HEADER  = '\033[95m'
    BLUE    = '\033[94m'
    CYAN    = '\033[96m'
    GREEN   = '\033[92m'
    YELLOW  = '\033[93m'
    RED     = '\033[91m'
    BOLD    = '\033[1m'
    DIM     = '\033[2m'
    END     = '\033[0m'

def bold(t): return f"{C.BOLD}{t}{C.END}"
def green(t): return f"{C.GREEN}{t}{C.END}"
def yellow(t): return f"{C.YELLOW}{t}{C.END}"
def red(t): return f"{C.RED}{t}{C.END}"
def cyan(t): return f"{C.CYAN}{t}{C.END}"
def dim(t): return f"{C.DIM}{t}{C.END}"

STATE_FILE = Path("./setup_state.json")
ENV_FILE   = Path("./.env")

# ── State management ──────────────────────────────────────────────────────────
def load_state() -> dict:
    if STATE_FILE.exists():
        return json.loads(STATE_FILE.read_text())
    return {
        "completed_steps": [],
        "credentials": {},
        "started_at": datetime.now().isoformat(),
        "last_run": None,
        "github_repo": None,
        "github_username": None,
    }

def save_state(state: dict):
    STATE_FILE.write_text(json.dumps(state, indent=2))

def mark_done(state: dict, step: str):
    if step not in state["completed_steps"]:
        state["completed_steps"].append(step)
    save_state(state)

def is_done(state: dict, step: str) -> bool:
    return step in state["completed_steps"]

def save_cred(state: dict, key: str, value: str):
    state["credentials"][key] = value
    save_state(state)
    # Also write to .env file
    write_env(state)

