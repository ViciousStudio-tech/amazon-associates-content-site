# ── UI helpers ─────────────────────────────────────────────────────────────────
def header(title: str, subtitle: str = ""):
    width = 62
    print()
    print("┌" + "─" * width + "┐")
    print(f"│  {bold(title):<{width-2}}│")
    if subtitle:
        print(f"│  {dim(subtitle):<{width-2}}│")
    print("└" + "─" * width + "┘")

def section(emoji: str, title: str):
    print(f"\n{bold(f'{emoji}  {title}')}")
    print("─" * 50)

def pause(msg: str = "Press ENTER when done...") -> str:
    print(f"\n{yellow('⏸  ACTION NEEDED')} → {msg}")
    return input(f"{C.YELLOW}   → {C.END}").strip()

def ask(prompt: str, required: bool = True, secret: bool = False, example: str = "") -> str:
    """Prompt user for input. Loops until valid if required."""
    if example:
        print(f"   {dim(f'Example: {example}')}")
    while True:
        try:
            if secret:
                import getpass
                val = getpass.getpass(f"{cyan('  ▶')} {prompt}: ").strip()
            else:
                val = input(f"{cyan('  ▶')} {prompt}: ").strip()
        except (KeyboardInterrupt, EOFError):
            print(f"\n{yellow('Paused. Re-run this script to continue.')}")
            sys.exit(0)
        if val or not required:
            return val
        print(f"   {red('This field is required. Try again.')}")

def open_url(url: str, label: str = ""):
    """Open a URL in the default browser."""
    print(f"\n   {cyan('→')} Opening: {bold(url)}")
    if label:
        print(f"   {dim(label)}")
    time.sleep(0.5)
    webbrowser.open(url)
    time.sleep(1)

def step_header(num: int, total: int, title: str, status: str = ""):
    badge = green("✓ DONE") if status == "done" else yellow("→ NEXT") if status == "current" else dim("○ LATER")
    print(f"\n  [{num}/{total}] {bold(title)}  {badge}")

def progress_bar(state: dict, steps: list):
    done = sum(1 for s in steps if is_done(state, s["id"]))
    total = len(steps)
    pct = int((done / total) * 40)
    bar = "█" * pct + "░" * (40 - pct)
    print(f"\n  Progress: [{green(bar)}] {done}/{total} steps complete")

def print_status(state: dict, steps: list):
    header("SETUP STATUS", "Amazon Associates Content Site")
    progress_bar(state, steps)
    print()
    for i, step in enumerate(steps, 1):
        done = is_done(state, step["id"])
        icon = green("✓") if done else dim("○")
        print(f"  {icon}  {step['name']}")

    print(f"\n  {dim('Credentials saved:')}")
    for k, v in state.get("credentials", {}).items():
        masked = v[:8] + "..." if len(v) > 8 else "***"
        print(f"     {k} = {masked}")

    if ENV_FILE.exists():
        print(f"\n  {green('.env file exists')}: {ENV_FILE.absolute()}")

