def step_first_pipeline_run(state: dict):
    """Run the first article pipeline."""
    section("🚀", "STEP 10: First Pipeline Run")
    print("  Running: keyword finder → article writer → publisher")
    print("  This will write your first 3 articles and push them live.")
    print()

    creds = state.get("credentials", {})
    missing = []
    required = ["ANTHROPIC_API_KEY", "GITHUB_TOKEN", "AMAZON_ASSOCIATE_TAG"]
    for k in required:
        if not creds.get(k):
            missing.append(k)

    if missing:
        print(f"  {red('Missing credentials:')} {', '.join(missing)}")
        print(f"  {yellow('Go back and complete those steps first.')}")
        return

    print(f"  {green('All required credentials present. Starting pipeline...')}\n")

    for script in ["b1_keyword_finder.py", "b1_article_writer.py", "b1_publisher.py"]:
        if not Path(script).exists():
            print(f"  {yellow('⚠')} {script} not found — make sure you unzipped the project files here")
            continue

        print(f"  Running {script}...")
        start = time.time()
        try:
            result = subprocess.run(
                [sys.executable, script],
                timeout=300,
                text=True
            )
            elapsed = round(time.time() - start, 1)
            if result.returncode == 0:
                print(f"  {green('✓')} {script} completed in {elapsed}s")
            else:
                print(f"  {red('✗')} {script} failed — check logs/ folder for details")
                cont = ask("Continue to next step anyway? (yes/no)")
                if cont.lower() not in ("yes","y"):
                    return
        except subprocess.TimeoutExpired:
            print(f"  {red('✗')} {script} timed out (>5min). Check logs/")

    username = state.get("github_username", "YOUR_USERNAME")
    site_url = f"https://{username}.github.io/amazon-associates-content-site"
    print(f"""
  {green('🎉 Pipeline complete!')}

  Your site: {bold(site_url)}
  Articles published: check _posts/ folder
  Database: data/business1.db

  GitHub will rebuild your site in 2-3 minutes.
  First articles appear in Google within 3-7 days.
    """)
    mark_done(state, "first_pipeline_run")


